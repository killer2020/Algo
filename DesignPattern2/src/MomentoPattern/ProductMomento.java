package MomentoPattern;

public class ProductMomento
{

	
	private String name;
	private double price;

	public ProductMomento(String name, double price)
	{
		this.name = name;
		this.price = price;
	}

	public String getName()
	{
		return name;
	}

	public double getPrice()
	{
		return price;
	}
	
	
	
	
}
